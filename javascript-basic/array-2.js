//add and remove items in array//

let fruits=["apple","mango",'man',]


//push and pop//

fruits.push('watermelon','jjjkj','jjjjhggg')
console.log(fruits)

fruits.pop()
console.log(fruits)

  ////if u have to add element in front we use push and pop and if we have to add at end of array we use shift and unshift///
///shift and unshift//

fruits.shift()      //// removes first element//
console.log(fruits)

fruits.unshift('appleee','mango00')         //// adds from first  element//
console.log(fruits)
